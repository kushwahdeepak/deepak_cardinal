            </span>
        </p>
        <p style="padding-left: 40px;">
            <em>
                <span style="font-weight: 400;">
                    “I recently interviewed the head of talent
                    acquisition for a large pharma company. She told
                    me that the science of genetics is advancing so
                    fast that they can no longer recruit for
                    specific positions or jobs. Rather, her teams
                    now look for people with advanced scientific
                    expertise and recruit them into jobs needing
                    these skills. This is a big trend in AI, machine
                    learning, and cyber, where the core disciplines
                    are advancing so fast that companies can’t hire
                    for specific positions.”
