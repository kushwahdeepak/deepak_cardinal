                people for potential and capabilities – not
                experience. Their ATS is not enough.
            </span>
        </p>
        <h2>ATS + AI = Talent Intelligence</h2>
        <p>
            A system of talent intelligence, on the other hand, is
            designed to hire for potential. It considers an
            applicant’s “adjacent skills” – enabling them to succeed
            in an open role.
        </p>
        <p>
            <span style="font-weight: 400;">
                Here are some of what companies can do by pairing an
                ATS with artificial intelligence:
            </span>
        </p>
        <h3>
            <span style="font-weight: 400;">
                Add Deep-learning AI to Searches
            </span>
        </h3>
        <p>
            <span style="font-weight: 400;">
                Adding the deep-learning capabilities of AI to
                searches helps bring people with the potential to do
                the job to the surface. By identifying adjacent
                skills and delivering far more relevant profiles, an
                employer increases the number of qualified
                candidates in their talent pipeline.
            </span>
        </p>
        <h3>
            <span style="font-weight: 400;">
                Use AI to Enable Talent Rediscovery
            </span>
        </h3>
        <p>
            <span style="font-weight: 400;">
                AI helps find people in a “Talent Network,” which
                includes past applicants, employee referrals, and
                “silver medalists” who may not have been a fit
                before but are now. We’ve found that companies that
                pair AI with an applicant tracking system find that
                roughly one-third of new hires coming from past
                applicants.
            </span>
        </p>
        <h3>
            <span style="font-weight: 400;">
                Building a Talent Network
            </span>
        </h3>
        <p>
            <span style="font-weight: 400;">
                A Talent Network is more than a database of past
                applicants. It can include employee referrals,
                ex-employees, and even current employees. In fact,
                we’ve seen from our customers that a Talent Network
                averages roughly about 200 times the number of
                employees at a company. So, as a rule of thumb, if
                you have 10,000 employees, you have something on the
                order of a 2 million person Talent Network when
                pairing an ATS with a capable AI platform. And more
                data equals better search results and more qualified
                candidates.
            </span>
        </p>
        <h3>
            <span style="font-weight: 400;">Make Matches</span>
        </h3>
        <p>
            <span style="font-weight: 400;">
                With AI, companies can see who the strongest matches
                are for any job, regardless of whether these matches
                are past applicants, ex-employees, passive/sourced
