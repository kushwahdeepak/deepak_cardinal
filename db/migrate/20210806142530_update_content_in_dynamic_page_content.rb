            </span>
        </p>
        <p>
            <span style="font-weight: 400;">
                The reality is that companies design ATSs to hire
                people who’ve done a job similar to one that’s open
                today. But for the companies whose businesses are
                changing rapidly, hiring someone based on specific
                experience isn’t sufficient. Take, for example, this
                quote from the recent
            </span>
            <a
